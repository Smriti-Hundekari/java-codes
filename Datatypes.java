public class Datatypes {
    public static void main(String a[])
    {
        int num1 = 7;
        byte b = 5;
        short s = 4;
        long l = 2345l;
        float f = 7.5f;
        double d = 7.5;
        char c = 's';
        boolean b2 = true;

        System.out.println(num1 + ", " + b + ", " + s + ", " + l + ", " + f + ", " + d + ", " + c + ", " + b2);
    }
}


